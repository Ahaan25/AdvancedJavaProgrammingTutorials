class CircleArea{
    public static void area(int r){
        double area;
        area = 3.14 * r * r;
        System.out.println("The area of the circle is:" + area);
    }
    public static void perimeter(int r){
        double perimeter;
        perimeter=3.14*2*r;
        System.out.println("The perimeter of the circle is:" + perimeter);
    }
}